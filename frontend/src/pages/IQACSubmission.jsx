const toDateRange = (event) => {
  const start = event?.requisition?.step1?.eventStartDate || event?.date || '-';
  const end = event?.requisition?.step1?.eventEndDate || event?.date || '-';
  const startTime = event?.requisition?.step1?.eventStartTime || event?.startTime || '';
  const endTime = event?.requisition?.step1?.eventEndTime || event?.endTime || '';

  let range = (start === end || end === '-') ? start : `${start} - ${end}`;
  if (startTime && endTime) {
    range += ` (${formatTime12(startTime)} - ${formatTime12(endTime)})`;
  }
  return range;
};

const yesNo = (value) => (String(value || '').toLowerCase() === 'yes' ? 'Yes' : 'No');

const isEventEndTimePassed = (event) => {
  if (!event) return false;
  const eventDate = event.date || event.requisition?.step1?.eventStartDate;
  const endTime = event.endTime || event.requisition?.step1?.eventEndTime;
  if (!eventDate || !endTime) return false;

  const [hours, minutes] = String(endTime).split(':').map(Number);
  const eventEnd = new Date(eventDate);
  eventEnd.setHours(hours, minutes, 0, 0);

  const now = Date.now();
  const endMs = eventEnd.getTime();
  const sevenDaysAfter = endMs + (7 * 24 * 60 * 60 * 1000);

  // Must be after event end AND within 7 days
  return now > endMs && now <= sevenDaysAfter;
};

